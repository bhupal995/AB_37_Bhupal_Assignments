
package assignments;

public class Assignment_4 {
	// Create many static method and call in inside a main method
	

	static void add() 
	{
	
		int a=10;
		int b=20;
		int sum=a+b;
		System.out.println(sum);
	}
	
	static void mul()
	{
		int a =25;
		int b =35;
		int mul = a*b;
		System.out.println(mul);
	}
	
	public static void main(String[] args) {
	
			add();
			mul();
	}

}
